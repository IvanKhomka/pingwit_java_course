package com.pingwit.part1;

public class ShortenedExample {
    public static void main(String[] args) {
        int x = 100;
        int sum = 0;
        sum = sum + x;
        System.out.println(sum);
        sum += x;
        System.out.println(sum);
        sum *= x;
        System.out.println(sum);
        sum /= x;
        System.out.println(sum);
    }
}
